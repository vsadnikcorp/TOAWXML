    public partial class frmTacFile : Form
    {
        public frmTacFile()
        {
            InitializeComponent();
        }

        private void btnCreateTacFile_Click(object sender, EventArgs e)
        {
            //string xmlPath = AppDomain.CurrentDomain.BaseDirectory.ToString() + "cities.xml";

            if (System.IO.File.Exists("FilePath.txt"))
            {
                string filePath = File.ReadAllText("FilePath.txt");
                Globals.GlobalVariables.PATH = System.IO.Path.Combine(filePath);

                string tacFilePath = filePath.Substring(0, filePath.Length - 3) + "tam";
                //XDocument xdoc = XDocument.Load(Globals.GlobalVariables.PATH);

                XDocument tacFile = new XDocument(
                   new XElement("GAME",
                       new XElement("OOB",
                           new XElement("FORCE", new XAttribute("ID", ""), new XAttribute("NAME", ""),
                             new XElement("FORMATION", new XAttribute("ID", ""), new XAttribute("NAME", ""), new XAttribute("LEADER",""), new XAttribute("FORMDATE", ""),
                                 new XElement("UNIT", new XAttribute("ID", ""), new XAttribute("NAME", ""), new XAttribute("LEADER", ""), new XAttribute("FORMDATE", ""), new XAttribute("LOSSES", ""), new XAttribute ("KILLS", ""),
                                     new XElement("EQUIPMENT", new XAttribute("ID", ""), new XAttribute("NAME", ""))))))));

                tacFile.Save(tacFilePath);
            }
        }
    }














































































































































































































































































































































































































































































































